//Create a loop that logs the numbers from 0-99 (ascending)
for (let i = 0; i <= 99; i++) {
    console.log(i);
}
//Create a loop that logs the numbers from 99-0 (descending)
for (let i = 99; i >= 0; i--) {
    console.log(i);
}
//Create a loop that logs the EVEN numbers from 0-98 (ascending)
